    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        clearTable();
        DefaultTableModel model;
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select bookid,name,author,publisher,capacity from book");
            
            while(rs.next()){
                String bookid = rs.getString(1);
                String name = rs.getString(2);
                String author = rs.getString(3);
                String publisher = rs.getString(4);
                String capacity = rs.getString(5);
                
                String availability;
                
                if(Integer.valueOf(capacity)>0)
                {
                    availability=capacity + " books available.";
                }
                else
                {
                    availability="Book not available.";
                }
                
                Object[] obj ={bookid,name,author,publisher,availability};
                model = (DefaultTableModel)jTable1.getModel();
                model.addRow(obj);     
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

