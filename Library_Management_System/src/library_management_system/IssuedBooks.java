    public static int calculatefine(String sid,String bid)
    {
        int days=0;
        int fine=0;
        try
        {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement st=con.prepareStatement("select returneddate,duedate from issue where studentid=? and bookid=?");
            st.setString(1,sid);
            st.setString(2,bid);
            ResultSet rs=st.executeQuery();
            String rd="";
            String dd="";
            while(rs.next())
            {
                rd=rs.getString(1);
                dd=rs.getString(2);
            }
            if(rd.matches("-"))
            {
                st=con.prepareStatement("select trunc(sysdate-to_date(?,?)) from dual");
                st.setString(1,dd);
                st.setString(2, "DD-MON-YYYY");
                rs=st.executeQuery();
            }
            else
            {
                st=con.prepareStatement("select trunc(to_date(?,?) - to_date(?,?)) from dual");
                st.setString(1,rd);
                st.setString(2, "DD-MON-YYYY");
                st.setString(3,dd);
                st.setString(4, "DD-MON-YYYY");
                rs=st.executeQuery();
            }
            while(rs.next())
            {
                days=Integer.valueOf(rs.getString(1));
            }
            if(days<=7)
            {
                fine=days;
            }
            else if(days>7&&days<=14)
            {
                fine = 7 + (days-7)*3;
            }
            else if(days>14)
            {
                fine = 7 + 7*3 + (days-14)*5;
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return fine;
    }
    
    public void setIssuedBooksToTable()
    {
        DefaultTableModel model;
        try{
            Connection con=ConnectionProvider.getCon();
            PreparedStatement st=con.prepareStatement("select studentid from log_student where log_id in (select max(log_id) from log_student)");
            ResultSet rs=st.executeQuery();
            String sid="";
            while(rs.next())
            {
                sid=rs.getString(1);
            }
            
            st = con.prepareStatement("select bookid,issueddate,duedate,returneddate from issue where studentid=?");
            st.setString(1,sid);
            rs = st.executeQuery();
            
            model = (DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            
            while(rs.next()){
                String bookid = rs.getString(1);
                String issueddate = rs.getString(2);
                String duedate = rs.getString(3);
                String returneddate = rs.getString(4);
                String returnedstatus="YES";
                if(returneddate.matches("-"))
                {
                    returnedstatus="NO";
                }
                String fine=String.valueOf(calculatefine(sid,bookid));
                Object[] obj ={bookid,issueddate,duedate,returnedstatus,fine};
                model.addRow(obj);     
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
